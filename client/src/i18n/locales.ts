export type TranslationKey = keyof typeof translations.en;

export function isLocale(value: string): value is Locale {
  return locales.includes(value as Locale);
}

export function localeDirection(locale: Locale): Direction {
  return localeOptions.find((option) => option.code === locale)?.direction || "ltr";
}

export function normalizeLocale(value: string | null | undefined): Locale {
  if (!value) {
    return defaultLocale;
  }

  if (isLocale(value)) {
    return value;
  }

  const normalized = value.toLowerCase();
  if (normalized.startsWith("fa") || normalized.startsWith("prs")) {
    return "fa-AF";
  }

  if (normalized.startsWith("ps")) {
    return "ps-AF";
  }

  return defaultLocale;
}
